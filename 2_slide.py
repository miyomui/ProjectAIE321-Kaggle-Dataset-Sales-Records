graph1.create_Adjmat("A")   # 2. ‡πÉ‡∏™‡πà‡∏ä‡∏∑‡πà‡∏≠ A
graph1.create_Adjmat("B")   # 3. ‡πÉ‡∏™‡πà‡∏ä‡∏∑‡πà‡∏≠ B
graph1.create_Adjmat("C")   # 4. ‡πÉ‡∏™‡πà‡∏ä‡∏∑‡πà‡∏≠ C
